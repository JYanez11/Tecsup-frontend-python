# Definimos la función saludar() que requiere de un parámetro
def saludar(nombre):
    print(f"Hola {nombre}")

# Ejecutamos la función saludar()
# y le pasamos el valor para el parámetro "nombre"
saludar('Marco')

# Definimos la función presentarse()
# que requiere de dos parámetros: nombre, profesion
def presentarse(nombre, profesion):
    print(f"Me llamo {nombre}, y soy {profesion}")

# Ejecutamos la función presentarse()
presentarse('Abel','contador')